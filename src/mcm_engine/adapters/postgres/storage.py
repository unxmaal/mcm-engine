    def bump_sequences(self) -> None:
        """Advance every owned table's IDENTITY sequence past its current
        MAX(id). Required after a migration that inserted explicit ids,
        so the next auto-generated id doesn't collide.
        """
        id_tables = [
            "knowledge", "negative_knowledge", "errors", "rules",
            "sessions", "snapshots", "relations",
        ]
        with self._conn.cursor() as cur:
            for table in id_tables:
                cur.execute(
                    f"SELECT setval("
                    f"  pg_get_serial_sequence(%s, 'id'),"
                    f"  COALESCE((SELECT MAX(id) FROM {table}), 1),"
                    f"  (SELECT MAX(id) FROM {table}) IS NOT NULL"
                    f")",
                    (table,),
                )
        self._conn.commit()

