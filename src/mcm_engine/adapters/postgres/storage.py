    def count_recent_knowledge(
        self, since_days: float, *, caller: Optional[str] = None,
    ) -> int:
        # Days-based recency match (sqlite uses julianday; postgres uses
        # interval arithmetic).
        with self._conn.cursor() as cur:
            cur.execute(
                "SELECT COUNT(*) AS cnt FROM knowledge "
                "WHERE EXTRACT(EPOCH FROM (now() - created_at)) / 86400.0 < %s",
                (float(since_days),),
            )
            r = cur.fetchone()
        return r["cnt"]

    def count_stale_knowledge(
        self,
        threshold_days: float = 90.0,
        *,
        caller: Optional[str] = None,
    ) -> int:
        with self._conn.cursor() as cur:
            cur.execute(
                "SELECT COUNT(*) AS cnt FROM knowledge "
                "WHERE EXTRACT(EPOCH FROM (now() - created_at)) / 86400.0 > %s "
                "AND (last_hit_at IS NULL "
                "     OR EXTRACT(EPOCH FROM (now() - last_hit_at)) / 86400.0 > %s) "
                "AND pinned = FALSE",
                (float(threshold_days), float(threshold_days)),
            )
            r = cur.fetchone()
        return r["cnt"]
